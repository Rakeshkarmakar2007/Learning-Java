public class WrapperClass {
    public static void main(String[] args) {

        boolean a1 = true;
        Boolean b1 = a1;

        char a2 = 'h';
        Character b2 = a2;

        int a3 = 10;
        Integer b3 = a3;

        short a4 = 5;
        Short b4 = a4;

        double a5 = 1.5;
        Double b5 = a5;

        float a6 = 3.8f;
        Float b6 = a6;


        System.out.println(b1);
        System.out.println(b2);
        System.out.println(b3);
        System.out.println(b4);
        System.out.println(b5);
        System.out.println(b6);
    }
}
